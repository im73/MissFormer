def generate_time_step(week):
    #timeF in Informer
    index = 0 
    data_stamp = np.zeros((week * 7 * 24, 4))
    for w in range(1, week+1):
        for day in range(1, 8):
            for hour in range(1, 25):
                data_stamp[index][0] = hour
                data_stamp[index][1] = day
                data_stamp[index][3] = (w-1) * 7 + day
                # day of the month
                if data_stamp[index][3] > 59:
                    data_stamp[index][2] = 3
                elif data_stamp[index][3] > 31:
                    data_stamp[index][2] = 2
                else:
                    data_stamp[index][2] = 1
                index = index + 1
                    
    data_stamp = (data_stamp - np.mean(data_stamp, axis=0))/(np.max(data_stamp, axis=0)-np.min(data_stamp, axis=0))
    
    return data_stamp
                
class NYC_Speed_hour(Dataset):
    def __init__(self, root_path, flag='train', size=None, 
                 features='S', data_path='./hourly_speed_mat_2019_1.npz', 
                 target='OT', scale=True, inverse=False, timeenc=0, freq='h', cols=None, mask_percentage=0, impute = False, interval = 24):
        # size [seq_len, label_len, pred_len]
        # info
        
        if size == None:
            self.seq_len = 24*4*4
            self.label_len = 24*4
            self.pred_len = 24*4
        else:
            self.seq_len = size[0]
            self.label_len = size[1]
            self.pred_len = size[2]
        # init
        
        assert flag in ['train', 'test', 'val']
        type_map = {'train':0, 'val':1, 'test':2}
        self.set_type = type_map[flag]
        
        self.features = features
        self.target = target
        self.scale = scale
        self.inverse = inverse
        self.timeenc = timeenc
        self.freq = freq
        
        self.interval = interval
        self.root_path = root_path
        self.data_path = data_path
        self.impute = impute
        self.mask_percentage = mask_percentage
        print("impute: {}, mask_paercentage : {}".format(self.impute, self.mask_percentage))
        self.__read_data__()

    def __read_data__(self):
        
        
        dense_mat = np.load(os.path.join(self.root_path, 'hourly_speed_mat_2019_1.npz'))['arr_0']
        for month in range(2, 4):
            dense_mat = np.append(dense_mat, np.load(os.path.join(self.root_path, 'hourly_speed_mat_2019_{}.npz'.format(month)))['arr_0'], axis = 1)
        border1s = [0, 7*7*24 - self.seq_len,8*7*24  - self.seq_len]
        border2s = [7*7*24, 8*7*24, 10*7*24]
        # if self.pre_train:
        #     # Use all the data when pretrain
        #     border1 = border1s[0]
        #     border2 = border2s[-1]
        # else:
        
        # observations percentage greater than 0.6
        
        pos = np.sum(dense_mat != 0, axis = 1)
        pos = pos / dense_mat.shape[1]
        dense_mat = dense_mat[(pos>=(1-self.mask_percentage)) &(pos<=(1 - self.mask_percentage + 0.1)),:]
        print(dense_mat.shape)
        
        self.mask = (dense_mat != 0) + 0
        self.mask = np.expand_dims(self.mask, -1)
        
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]
        
        
        
        df_data = dense_mat
        self.datamean = 0
        self.datastd = 1
        if self.scale:
            dense_mat[dense_mat==0] = np.nan
            self.datamean = np.nanmean(dense_mat[:, border1s[0]:border2s[0]],axis = 1)
            self.datastd = np.nanstd(dense_mat[:, border1s[0]:border2s[0]], axis = 1)

            out = (dense_mat.T - self.datamean) / self.datastd

            out = out.T
            out[np.isnan(out)] = 0
            data = out

        else:
            data = dense_mat
            
        df_stamp = generate_time_step(10)
        self.data_stamp = df_stamp[border1:border2]

        self.data_x = data[:,border1:border2]
        print(self.data_x.shape)
        self.data_x = np.expand_dims(self.data_x, -1)
        self.data_y = data[:,border1:border2]
        self.data_y = np.expand_dims(self.data_y, -1)

        self.single_length = self.data_x.shape[1] - self.seq_len- self.pred_len + 1
    def __getitem__(self, index):
        new_index = index * self.interval
        road_seg = int(new_index / self.single_length)
        s_begin = new_index % self.single_length
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len 
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[road_seg][s_begin:s_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
         
        if self.inverse:
            seq_y = np.concatenate([self.data_x[r_begin:r_begin+self.label_len], self.data_y[r_begin+self.label_len:r_end]], 0)
        
        seq_y = self.data_y[road_seg][r_begin:r_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]
        
        mask = self.mask[road_seg][r_begin:r_end]
        
        return seq_x, seq_y, seq_x_mark, seq_y_mark, mask 
    
    def __len__(self):
        return ((self.data_x.shape[1] - self.seq_len- self.pred_len + 1))*self.data_x.shape[0] // self.interval 

    def inverse_transform(self, data):
        return self.scaler.inverse_transform(data)